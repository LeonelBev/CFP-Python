print("Ingrese la altura: ")
altura = float(input())
print("Ingrese la base: ")
base = float(input())
#Base * altura
superficie = altura * base
#(Base + altura) * 2
perimetro = (altura + base) * 2
print("Altura: ", altura, "| Base: ",altura)
print("Superficie: ", superficie, "| Perimetro: ", perimetro)